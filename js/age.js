for (let i = 0; i < 20; i++)
if (i < 2) {
    console.log("infant")
    i++
} else if (i === 2) {
    console.log("toddler")
} else if (i >= 3, i < 10) {
    console.log("child")
} else if (i >= 10, i < 13) {
    console.log('preteen')
} else if (i >= 13, i < 18) {
    console.log('teen')
} else {
    console.log('adult')
}
